// Utility to add jitter
export function addJitter(lat: number, lng: number, country: string): { lat: number, lng: number } {
  const spread = ['United States', 'India', 'Australia', 'Brazil'].includes(country) ? 2.5 : 0.5;
  const latOffset = (Math.random() - 0.5) * spread;
  const lngOffset = (Math.random() - 0.5) * spread;
  return { lat: lat + latOffset, lng: lng + lngOffset };
}